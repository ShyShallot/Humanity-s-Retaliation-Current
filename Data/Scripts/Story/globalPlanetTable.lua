---@param planet string|PlanetObject|nil
---@return nil|Planet_Entry
function Planet_Table:Get_Entry(planet)
    if planet == nil then
        return nil
    end

    if type(planet) ~= "string" then
        if TestValid(planet) then
            local Planet_Name = planet.Get_Type().Get_Name()

            return self:Get_Entry_From_String(Planet_Name)
        end
    else
        return self:Get_Entry_From_String(planet)
    end
end

---@private
---@param planet_name string|nil
---@return nil|Planet_Entry
function Planet_Table:Get_Entry_From_String(planet_name)
    if type(planet_name) ~= "string" then
        return nil
    end

    if tableLength(self.Planets) == 0 then
        return nil
    end

    return self.Planets[string.upper(planet_name)] -- will return nil or the table
end

--- Returns a table containing all keys of the Planet Table
---@return table
function Planet_Table:Return_All_Keys()
    local keys = {}

    if tableLength(self.Planets) == 0 then
        return keys
    end

    for key, _ in pairs(self.Planets) do
        table.insert(keys, key)
    end

    return keys
end

---@param planet PlanetObject|string
---@return string|nil
function Planet_Table:Get_Planet_String(planet)
    if planet == nil then
        return ""
    end

    local Planet_Entry = nil

    if type(planet) == "string" then
        Planet_Entry = self.Planets[string.upper(planet)]
    end

    if planet.Get_Type ~= nil then
        local Planet_Name = planet.Get_Type().Get_Name()

        if type(Planet_Name) == "string" then
            Planet_Entry = self.Planets[string.upper(Planet_Name)]
        end
    end

    if Planet_Entry ~= nil then
        if Planet_Entry.String == nil then
            return ""
        end

        return Planet_Entry.String
    end
end

return Planet_Table
